#include <libft.h>

size_t	ft_intlen(long int n)
{
	size_t		len;

	len = 1;
	if (n < 0)
	{
		n *= -1;
		++len;
	}
	while (n >= 10)
	{
		++len;
		n /= 10;
	}
	return (len);
}
